					About the College of Theology Library
				</h1>
				<p class="mbr-text align-center pb-3 mbr-fonts-style display-5">The College of Theology Library is one of the department libraries that comprise the Central Philippine University Libraries. The other components are: Henry Luce III Library (Main Library) and other departmental libraries, namely: College of Law Library, Graduate Studies Library, High School Library and Elementary Library. The department libraries are housed in their departments except the Theology Library and Graduate Studies Library which are housed at Henry Luce III Library.</p>
				<hr class="divider">
				<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">The Theology Library maintains a collection of relevant books and periodicals in religion and related subject areas. The Theology Library holds about 15,288 titles and 19, 149 volumes of books organized and arranged according to the Dewey Decimal Classification (DDC) System. Added to this is the rich collection of the main Library of 240,841 volumes of print and non-print materials to supplement to the study and research needs of faculty and seminary. It also provides supplementary materials in philosophy, psychology, humanities, Filipiniana, women’s studies general references, etc. The College of Theology Library and Main Library have a wide selection of music pieces. Other non-book materials such as CD’s and DVD’s are also available for to be used as additional instructional materials. The data were already encoded and could be retrieved in the computer using the OPAC (Online Public Catalog) of the Main Library. Theology Library has copies of dictionaries in Pilipino and other dialects and materials in Philippine literature at the Filipiniana collection section of the Theology Library and Main Library respectively.</p>
				<hr class="divider">
				<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">The College of Theology Library has a special collection on feminist Theology called Women’s Studies Collection. Currently, the collection has 248 titles and volumes of books. In addition, the Main Library has also collection of women’s studies, gender issues and person with special needs which are available for students use. There are also periodicals journals-local and foreign subscription related to the subject matter as supplement resources for faculty and seminary students to enhance their learning in class. There are more than 1, 000 bound periodicals in Religion and Theology in this collection. The library subscribes to 30 foreign and 14 local journals. Theology Library also received foreign and local gifts and exchange sent to us by publishers for free or as a gift from alumni and friends. The library acquires these materials through purchases and gifts from alumni and friends here abroad.</p>
