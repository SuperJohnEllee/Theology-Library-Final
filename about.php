				<h1 class="mbr-section-title align-center mbr-bold pb-3 mbr-fonts-style display-5">
					Brief History of the College of Theology
				</h1>
				<p class="mbr-text align-center pb-3 mbr-fonts-style display-5">The early beginning of the College of Theology can be traced to the Bible School that was conceived together with that of the Jaro Industrial School, the precursor of Central Philippine University. The idea to establish two schools was conceived when, on December 2, 1904 when the Philippine Baptist Mission Conference, the organization of the early American Baptist Missionaries, voted "to established two schools: an industrial school for boys and a Bible School to train pastors and other Christian Workers." The Rev. William O. Vallentine was appointed administrator of the two schools.</p>
				<hr>
				<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">In June 1905, the Bible School was opened in the home of the Valentines. There were 12 pupils with some "Bible Women" who attended as auditors. On October 1, 1905, the Jaro Industrial School was opened on a 24-hectare land in Jaro. The leadership of the Bible School was turned over to the Rev. Henry Munger who conducted classes off campus. In 1907, Dr. Eric Lund became the principal and classes were held at the Mission Press building where Lund was doing his Scripture translation work. When Dr. Lund left in 1912, the Bible School was closed. It was reopened in 1913 by Rev. Alton Bigelow. It was under his leadership that the school began to have a definite direction in its development.</p>
				<hr>
				<a class="btn btn-primary" data-toggle="collapse" href="#collapse"><span class="fa fa-plus" role="button" aria-expanded="false" aria-controls="collapse"></span> Show More Info</a>
				<div class="collapse" id="collapse">
					<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">In 1923, the Jaro Industrial School became Central Philippine College. In April of the following year, the Bible school became an organic part of the College. A program leading to the degree of Bachelor of Theology was offered. Those who enrolled were men students who were trained to be pastors of the churches. In 1936, through the guidance of Rev. Bigelow, the school became the Department of Theology of Central Philippine College.</p>
					<hr>
					<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">While the Bible School was established for the training of men to be pastors, the training of women to be missionaries began in 1904 with the establishment of the Baptist Missionary Training School(BMTS) under the leadership of Miss Anna V. Johnson and Miss Celia Sainz. Both were sent to the Philippines by the Women's American Baptist Foreign Mission Society. Miss Johnson was assigned in Capiz. The <abbr title="Baptist Missionary Training School">BMTS</abbr> continued to develop separately from the Bible School until 1938, when it was merged with the Department of Theology of Central Philippine College. With the merging of the schools, two degree programs were offered - the Bachelor of Theology and the Bachelor of Science in Religious Education.</p>
					<hr>
					<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">On April 1, 1953, Central Philippine College was granted government recognition as a university. Following this, the Department of Theology became the College of Theology. Gradually, the leadership of the university and the College of Theology was turned over to the Filipinos. A significant part of this Fipinization process took place in 1966 when Dr. Rex D. Drilon became the first Filipino President of thr Univeristy</p>
        			<hr>
        			<p class="mbr-text align-center mbr-light pb-3 mbr-fonts-style display-5">The College of Theology is an integral part of Central Philippine University. Its purpose is to train pastors and Christian leaders for the Christian churches, particularly the churches of the Convention of Philippine Baptist Churches. It cooperates with evangelical and ecumenical organizations here and abroad. It is a member institution of the Association for Theological Education in South Easg Asia(SEAGIST) and the <i>Samahan Ng Teolohikal na Edukasyon ng Pilipinas</i></p>
